public class Variables {
    public static void main(String[] args){

            // Primitive data types

            byte Age = 21; // Size:1 byte Range: −128 to 127

            short Year = 2025; // Size:2 byte Range: −32,768 to 32,767

            int Id = 1; // Size:4 byte Range: −2³¹ to 2³¹−1

            long Population = 8000000000L; // Size:8 byte Range: Very large numbers

            float Quotient = 23.4f; // Size:4 byte Range: Decimal (less precise)

            double Average = 34.566666; // Size:8 byte Range: Decimal (more precise)

            char Gender = 'M'; // Size:2 byte Range: Single character

            boolean isStudent = true; // Size:1 byte Range: true / false


            System.out.println("Age: "+Age);
            System.out.println("Year: "+Year);
            System.out.println("Id: " + Id);
            System.out.println("Population: "+Population);
            System.out.println("Quotient: " + Quotient);
            System.out.println("Average: " + Average);
            System.out.println("Gender: " + Gender);
            System.out.println("Is Student: " + isStudent);


            // Non-Primitive (Reference) Data Types
/*
        Type        | Description
        -----------------------------
        String      | Stores text
        Array       | Stores multiple values
        Class       | Blueprint for objects
        Object      | Instance of a class
        Interface   | Contract for classes
*/

    }
    
}
